package com.example.Library.Management.system.Controller;

import com.example.Library.Management.system.Entities.LibraryCard;
import com.example.Library.Management.system.Service.CardService;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.http.HttpStatus;
import org.springframework.http.ResponseEntity;
import org.springframework.web.bind.annotation.*;

@RestController
@RequestMapping("card")
public class CardController {

    @Autowired
    CardService cardService;

    @PostMapping("generate-plain-card")
    public ResponseEntity generatePlainCard(){
        //cardNo : autogenerated
        //cardStatus: default as new
        //cardName: as student is associated so will generate with an API
        LibraryCard newCard = cardService.generateCard();
        String response = "The new card is generated with card no.:"+ newCard.getCardId();
        return new ResponseEntity(response, HttpStatus.CREATED);
    }

    @PutMapping("/associate-with-student")
    public ResponseEntity associateWithStudent(@RequestParam("studentId") Integer studentId, @RequestParam("cardId") Integer cardNo){
        String result = cardService.associateWithStudent(studentId,cardNo);
        return new ResponseEntity(result,HttpStatus.OK);
    }
}
